package DTO;

/**
 *
 * @author hendrix
 */
public class People {
    int ID;
    String name;
    int age;
    String job;

    public People(int ID, String name, int age, String job) {
        this.ID = ID;
        this.name = name;
        this.age = age;
        this.job = job;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getJob() {
        return job;
    }

    public void setJob(String job) {
        this.job = job;
    }
    
}
